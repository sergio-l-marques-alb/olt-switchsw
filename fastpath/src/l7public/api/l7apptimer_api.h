#define APPTIMER_DEBUG   

#ifdef APPTIMER_DEBUG
#undef APPTIMER_DEBUG
#endif

#define APPTIMER_STR_LEN 10
typedef struct
{
  L7_sll_member_t *next;      /* Link to next entry */
  L7_uint32       expiryTime; /* Expiry time of this entry */
  L7_app_tmr_fn   expiryFn;
  void            *pParam;
#ifdef APPTIMER_DEBUG  
  L7_uchar8       name[APPTIMER_STR_LEN];
#endif
} timerNode_t;

